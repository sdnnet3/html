									<h1>World Famous<br />
									San Diego Zoo</a></h1>
									<p>5 reasons to visit the San Diego Zoo<br /></p>
									<p>Written by Clayton Hutton</p>
									<br /><hr />
								</header>

								<div class="card border-0">
								<p style="margin: 2%;"><span class="image left fluid"><img style="border-radius: 15px;" src="../images/headline/IMG_0280.jpg" alt="Sky-shot" /></span>
									<b>1. </b>The first reason? Right off the bat, this place is huge. Not just a little huge, but a lot huge. This zoo comes in at about 100 acres, leaving you with plenty of ground to cover. So to make the most of this experience, you should come early and well rested (if you have kids, feel free to ignore this advice... we all know there's no such thing as rest). There is plenty of shade in the park mixed in with the park; so taking a route that lets you take a break from the sun is a great idea. If you really want to get the most out of the zoo, pick up a year pass and make a second trip. You will get to really take it easy and make it more about the experience instead of trying to get in all the animals you can in one day.</p><br />
								</div><hr />

								<div class="card border-0">
								<p style="margin: 2%;"><span class="image right"><img style="border-radius: 15px;" src="../images/headline/IMG_0317.jpg" alt="Sky-shot" /></span>
									<b>2. </b>"The Institute for Conservation Research is one of the largest zoo-based research centers in the world." This institute has been around since 1975 and has exceled at developing and establishing methods that help them build self-sustaining populations in the wild. When you visit this zoo, you are helping to preserve endangered species and helping pay for research with that goal.</p><br />
								</div><hr />
								
								<div class="card border-0">
								<p style="margin: 2%;"><span class="image left"><img style="border-radius: 15px;" src="../images/headline/IMG_0329.jpg" alt="Polor Bear Statue" /></span>
									<b>3. </b>I feel like this might reason might be cheating, but I have kids. The statues littered about the park are fantastic, well maintained and have plenty of shade to keep little hands from burning. My personal favorite is the polar bear, but the elephants come in a close second just because the kids have a blast pretending to be squished by a large elephant.</p><br />
								</div><hr />

								<div class="card border-0">
								<p style="margin: 2%;"><span class="image right"><img style="border-radius: 15px;" src="../images/headline/IMG_0374.jpg" alt="Sky-shot" /></span>
									<b>4. </b>Probably my favorite species at the zoo, the Meerkat. This guy is constantly on the lookout for skyward predators. You can usually see two or three of them on guard duty while the others relax in the sun. As cute as they are, I'm not sure you'd want them in your front yard as they are expert burrowers.</p><br />
								</div><hr />

								<div class="card border-0">
								<p style="margin: 2%;"><span class="image left"><img style="border-radius: 15px;" src="../images/headline/IMG_0392.jpg" alt="Sky-shot" /></span>
									<b>5. </b>Africa Rocks! Though it's somewhat near the back of the park, in direct heat of the mid-day sun, it doesn't seem to bother these African animals that much. The relatively newer enclosures here are much more resemblent of their natural, open environment. Compared to what the enclosure looked like in the past, it's definitely a step up from what it once was. The entire part of this park is very well thought out and looks amazing. It's a must stop spot on your next trip.</p><br />
